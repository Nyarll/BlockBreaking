//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Game::Update(void)
{
	frame_timer->Update();
	player->Update();
}



//----------------------------------------------------------------------
//! @brief ゲームの描画処理
//!
//! @param[in] なし
//!
//! @return なし
//----------------------------------------------------------------------
void Game::Render(void)
{
	DrawFormatString(60, 60, 0xffffffff, "FRAME RATE : %f", frame_timer->GetFrameRate());
	player->Render();
}



