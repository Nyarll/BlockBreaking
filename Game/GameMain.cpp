// メンバ関数の定義 ========================================================
//------------------------------------------------------------------
//! @brief デフォルトコンストラクタ
//!
//! @param[in] なし
//------------------------------------------------------------------
Game::Game()
{
	this->frame_timer = new FrameTimer;
	this->joy_pad = new Joypad;
	this->player = new Player;
}


//------------------------------------------------------------------
//! @brief デストラクタ
//------------------------------------------------------------------
Game::~Game()
{
	delete this->frame_timer;
	delete this->joy_pad;
	delete this->player;
}



